short Character::getDirectionality(int codePoint) {
  int head = 0, tail = direct_lst_size_ - 1;
  if (codePoint >= decode_codepoint(DirectLst[tail]) || codePoint < 0) {
    return -1;
  }
  while (head <= tail) {
    int mid = (head + tail) / 2;
    if (codePoint >= decode_codepoint(DirectLst[mid]) &&
        codePoint < decode_codepoint(DirectLst[mid + 1])) {
      return decode_val(DirectLst[mid]);
    }
    if (codePoint < decode_codepoint(DirectLst[mid])) {
      tail = mid - 1;
    } else {
      head = mid + 1;
    }
  }
  return -1;
}

int Character::getType(char32_t codePoint) {
  size_t head = 0;
  auto tail = type_lst_size_ - 1;
  if (codePoint < 0 || codePoint >= decode_codepoint(TypeLst[tail])) {
    return 0;
  }
  while (head <= tail) {
    auto mid = (head + tail) / 2;
    if (codePoint >= decode_codepoint(TypeLst[mid]) &&
        codePoint < decode_codepoint(TypeLst[mid + 1])) {
      return decode_val(TypeLst[mid]);
    }
    if (codePoint < decode_codepoint(TypeLst[mid])) {
      tail = mid - 1;
    } else {
      head = mid + 1;
    }
  }
  return 0;
}

int Character::decode_codepoint(uint32_t num) {
  auto val = num >> 8;
  return val;
}

uint8_t Character::decode_val(uint32_t num) {
  auto val = num & 0xff;
  if (val == 255) {
    return -1;  // the value -1 of direction is saved as 255
  }
  return val;
}
